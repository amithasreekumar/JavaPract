package basicprogram;

public class ConditionalStatements {

	public static void main(String[] args) {
		int num = 45;
		if (num >= 0) {
			System.out.println("positive");
		} else {
			System.out.println("negative");
		}
		
		int age = 50;
		if (age >= 18) {
			System.out.println("adult");
		} else {
			System.out.println("teenage");
		}
		

	}

}
